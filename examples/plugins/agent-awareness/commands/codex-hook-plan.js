const fs = require('fs')
const path = require('path')
const crypto = require('crypto')

const DEFAULT_PORT = 8795
const INGEST_TOKEN_FILE = 'ingest-token.txt'

const ensureDataDir = (context = {}) => {
  const dataDir = process.env.OPENPET_DATA_DIR || context.paths?.dataDir || ''
  if (!dataDir) throw new Error('OPENPET_DATA_DIR is required')
  fs.mkdirSync(dataDir, { recursive: true })
  return dataDir
}

const getServicePort = (context = {}) => {
  const value = Number(context.port || DEFAULT_PORT)
  return Number.isFinite(value) && value > 0 ? Math.floor(value) : DEFAULT_PORT
}

const getOrCreateIngestToken = (dataDir) => {
  const tokenPath = path.join(dataDir, INGEST_TOKEN_FILE)
  if (fs.existsSync(tokenPath)) return fs.readFileSync(tokenPath, 'utf-8').trim()
  const token = crypto.randomBytes(24).toString('base64url')
  fs.writeFileSync(tokenPath, `${token}\n`, { mode: 0o600 })
  return token
}

const createCodexHookInstructions = ({ port = DEFAULT_PORT } = {}) => `# OpenPet Agent Awareness Codex Hook Setup

This file is generated by the OpenPet Agent Awareness plugin.

OpenPet does not modify Codex configuration automatically. Review the command
below and wire it into your Codex hook mechanism only if you accept sending
sanitized status events to the local OpenPet service.

Service endpoint:

\`\`\`text
http://127.0.0.1:${port}/api/events
\`\`\`

Minimal event sender example:

\`\`\`sh
curl -sS -X POST "http://127.0.0.1:${port}/api/events" \\
  -H "Content-Type: application/json" \\
  -d '{
    "adapter": "codex",
    "sessionId": "'"$OPENPET_AGENT_SESSION_ID"'",
    "type": "agent.status",
    "status": "working",
    "message": "Agent is working",
    "cwd": "'"$PWD"'"
  }'
\`\`\`

Do not send prompts, tool inputs, terminal transcripts, stdout, stderr, API keys,
or full file paths in \`message\`.
`

const writeCodexHookPlan = (context = {}) => {
  const dataDir = ensureDataDir(context)
  const port = getServicePort(context)
  const token = getOrCreateIngestToken(dataDir)
  const instructionsPath = path.join(dataDir, 'codex-hooks.manual.md')
  fs.writeFileSync(instructionsPath, createCodexHookInstructions({ port }).replace(
    '-H "Content-Type: application/json"',
    `-H "Authorization: Bearer ${token}" \\
  -H "Content-Type: application/json"`
  ))
  return {
    instructionsPath,
    tokenPath: path.join(dataDir, INGEST_TOKEN_FILE),
    serviceUrl: `http://127.0.0.1:${port}/api/events`
  }
}

const writeCodexHookRemovalPlan = (context = {}) => {
  const dataDir = ensureDataDir(context)
  const removalPath = path.join(dataDir, 'codex-hooks.removal.md')
  fs.writeFileSync(removalPath, `# OpenPet Agent Awareness Codex Hook Removal

OpenPet did not install Codex hooks automatically. Remove the manually added
hook command that posts to http://127.0.0.1:${getServicePort(context)}/api/events.

After removal, stop the Agent Awareness service from OpenPet Control Center.
`)
  return { removalPath }
}

module.exports = {
  createCodexHookInstructions,
  getOrCreateIngestToken,
  INGEST_TOKEN_FILE,
  writeCodexHookPlan,
  writeCodexHookRemovalPlan
}
