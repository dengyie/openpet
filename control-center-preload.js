  SETTINGS_GET: 'settings:get',
  SETTINGS_SAVE: 'settings:save',
  SETTINGS_IMPORT_CURSOR: 'settings:import-cursor',
  SETTINGS_PREVIEW_SCALE: 'settings:preview-scale',
  SETTINGS_CLOSE: 'settings:close',
  ACTIONS_GET: 'actions:get',
  ACTIONS_INSPECT_FRAMES: 'actions:inspect-frames',
  ACTIONS_REINSPECT_FRAMES: 'actions:reinspect-frames',
  ACTIONS_CLEAR_FRAME_SELECTION: 'actions:clear-frame-selection',
  ACTIONS_IMPORT_FRAMES: 'actions:import-frames',
  ACTIONS_SAVE_CONFIG: 'actions:save-config',
  ACTIONS_SUBMIT_TRIGGER_PROPOSAL: 'actions:submit-trigger-proposal',
  ACTIONS_ACCEPT_TRIGGER_PROPOSAL: 'actions:accept-trigger-proposal',
  ACTIONS_REJECT_TRIGGER_PROPOSAL: 'actions:reject-trigger-proposal',
  ACTIONS_DELETE: 'actions:delete',
  PET_PACKS_LIST: 'pet-packs:list',
  PET_PACKS_INSPECT_DIRECTORY: 'pet-packs:inspect-directory',
  PET_PACKS_CLEAR_SELECTION: 'pet-packs:clear-selection',
  PET_PACKS_IMPORT: 'pet-packs:import',
  PET_PACKS_EXPORT: 'pet-packs:export',
  PET_PACKS_SET_ACTIVE: 'pet-packs:set-active',
  PET_PACKS_ACTIVE_CHANGED: 'pet-packs:active-changed',
  PET_PACKS_REMOVE: 'pet-packs:remove',
  AI_GET_CONFIG: 'ai:get-config',
  AI_SAVE_CONFIG: 'ai:save-config',
  AI_SAVE_API_KEY: 'ai:save-api-key',
  AI_TEST_CONNECTION: 'ai:test-connection',
  AI_GET_PERSONA_PROFILE: 'ai:get-persona-profile',
  AI_GENERATE_PERSONA_DRAFT: 'ai:generate-persona-draft',
  AI_SAVE_PERSONA_OVERRIDE: 'ai:save-persona-override',
